public class InsuranceDetails {
    private String HealthInsuranceCompany;
    private String CorporateIDNumber;
    private String Address;
    private String Premium;
    private String Deductible;
    private String StatementOfBenefits;

    public String getHealthInsuranceCompany() {
        return HealthInsuranceCompany;
    }

    public void setHealthInsuranceCompany(String HealthInsuranceCompany) {
        this.HealthInsuranceCompany = HealthInsuranceCompany;
    }

    public String getCorporateIDNumber() {
        return CorporateIDNumber;
    }

    public void setCorporateIDNumber(String CorporateIDNumber) {
        this.CorporateIDNumber = CorporateIDNumber;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String Address) {
        this.Address = Address;
    }

    public String getPremium() {
        return Premium;
    }

    public void setPremium(String Premium) {
        this.Premium = Premium;
    }

    public String getDeductible() {
        return Deductible;
    }

    public void setDeductible(String Deductible) {
        this.Deductible = Deductible;
    }

    public String getStatementOfBenefits() {
        return StatementOfBenefits;
    }

    public void setStatementOfBenefits(String StatementOfBenefits) {
        this.StatementOfBenefits = StatementOfBenefits;
    }

    

    
}
